    //MÃ©todos
    public void agregarCurso(Curso curso) {
        cursos.add(curso);
        buscar("");
    }

    private void buscar(String busqueda) {
        this.modelListCursos.removeAllElements();

        for (Curso curso : cursos) {
            if (curso.getNombre() != null && curso.getNombre().contains(busqueda)) {
                this.modelListCursos.addElement(curso.getMostrarCurso());
            }
        }
    }

    private void salir() {
        padre.setVisible(true);
        this.dispose();
    }

